let currentImages = [];

function getUrlParameter(name) {
  const urlParams = new URLSearchParams(window.location.search);
  return urlParams.get(name);
}

function changeImage(index) {
  const mainImage = document.getElementById("main-image");
  const thumbnails = document.querySelectorAll(".thumbnail");

  // Remove active class from all thumbnails
  thumbnails.forEach((thumb) => thumb.classList.remove("active"));

  // Add active class to clicked thumbnail
  if (thumbnails[index]) {
    thumbnails[index].classList.add("active");
  }

  // Update main image with actual image element
  if (currentImages[index]) {
    mainImage.innerHTML = `<img src="${currentImages[index].src}" alt="${currentImages[index].alt}" style="max-width: 100%; height: auto; border-radius: 8px;">`;
  }
}

function loadLaptopDetails() {
  const laptopId = getUrlParameter("id");
  const mainContentElement = document.getElementById("main-content");
  const laptopNameElement = document.getElementById("laptop-name");

  console.log("Loading laptop details for ID:", laptopId); // Debug log

  // Check if laptop data exists
  if (!laptopId || !laptopData[laptopId]) {
    if (mainContentElement) {
      mainContentElement.innerHTML = `
        <div style="text-align: center; padding: 2rem;">
          <h2>Laptop Not Found</h2>
          <p>The requested laptop details could not be found.</p>
          <p>Laptop ID: ${laptopId || "Not provided"}</p>
          <a href="/">Back to Laptops</a>
        </div>
      `;
      mainContentElement.style.display = "block";
    }
    return;
  }

  const data = laptopData[laptopId];
  currentImages = data.images;

  console.log("Found laptop data:", data); // Debug log

  // Update breadcrumb
  if (laptopNameElement) {
    laptopNameElement.textContent = data.title.split(" - ")[0];
  }

  // Update page title
  document.title = `${data.title} - Premium Laptops`;

  // Update thumbnails with actual images
  const thumbnails = document.querySelectorAll(".thumbnail");
  data.images.forEach((image, index) => {
    if (thumbnails[index]) {
      thumbnails[
        index
      ].innerHTML = `<img src="${image.src}" alt="${image.alt}" style="width: 100%; height: 100%; object-fit: cover; border-radius: 4px;">`;
      thumbnails[index].setAttribute("title", image.alt);
    }
  });

  // Set first thumbnail as active and load first image
  if (thumbnails[0]) {
    thumbnails[0].classList.add("active");
  }
  const mainImage = document.getElementById("main-image");
  if (mainImage && currentImages[0]) {
    mainImage.innerHTML = `<img src="${currentImages[0].src}" alt="${currentImages[0].alt}" style="max-width: 100%; height: auto; border-radius: 8px;">`;
  }

  // Load product info
  const productInfoHtml = `
    <h1 class="title">${data.title}</h1>
    <p class="product-description">${data.description}</p>
    ${data.features
      .map(
        (feature) => `
        <div class="feature">
            <h3 class="feature-title">${feature.title}</h3>
            <p class="feature-description">${feature.description}</p>
        </div>
    `
      )
      .join("")}
  `;

  const productInfoElement = document.getElementById("product-info");
  if (productInfoElement) {
    productInfoElement.innerHTML = productInfoHtml;
  }

  if (mainContentElement) {
    mainContentElement.style.display = "block";
  }

  console.log("Laptop details loaded successfully"); // Debug log
}

// Load laptop details when page loads
document.addEventListener("DOMContentLoaded", loadLaptopDetails);
